/// Generates typed output entries from streamed scan events.
Stream<GeneratedEntry> generate(
  Stream<ScanEntry> entries,
  BuildConfig config, {
  bool includeRuntime = true,
  bool includeOpenApi = true,
  bool includeClient = true,
}) async* {
  final state = await collectScanState(entries);
  yield* _generateFromState(
    state,
    config,
    includeRuntime: includeRuntime,
    includeOpenApi: includeOpenApi,
    includeClient: includeClient,
  );
}

Stream<GeneratedEntry> _generateFromState(
  ScanState state,
  BuildConfig config, {
  required bool includeRuntime,
  required bool includeOpenApi,
  required bool includeClient,
}) async* {
  final outputDir = p.join(config.rootDir, config.outputDir);
  final genDir = p.join(outputDir, 'src');
  final sourceFiles = <String>{
    for (final entry in state.routes) entry.filePath,
    for (final entry in state.globalMiddleware) entry.filePath,
    for (final entry in state.scopedMiddleware) entry.filePath,
    for (final entry in state.scopedErrors) entry.filePath,
    ?state.fallback?.filePath,
    ?state.hooks?.filePath,
  }.toList()..sort();

  final aliases = <String, String>{};
  final imports = <String>[];
  for (var i = 0; i < sourceFiles.length; i++) {
    final filePath = sourceFiles[i];
    final relative = p.relative(filePath, from: genDir).replaceAll('\\', '/');
    final alias = '\$i$i';
    aliases[filePath] = alias;
    imports.add("import '$relative' as $alias;");
  }

  final routeGroups = <String, List<RouteEntry>>{};
  for (final route in state.routes) {
    routeGroups.putIfAbsent(route.path, () => <RouteEntry>[]).add(route);
  }

  final openapi = config.openapi;
  final hasDocsUi =
      openapi != null && openapi.ui != null && openapi.output.type == 'route';

  final app = StringBuffer()
    ..writeln("// Generated by spry - do not edit.")
    ..writeln(
      "import 'package:spry/spry.dart' show Spry, ErrorRoute, MiddlewareRoute;",
    );

  final usesHttpMethod =
      hasDocsUi ||
      routeGroups.values.any(
        (entries) => entries.any((it) => it.method != null),
      ) ||
      state.globalMiddleware.any((it) => it.method != null) ||
      state.scopedMiddleware.any((it) => it.method != null) ||
      state.scopedErrors.any((it) => it.method != null);
  if (usesHttpMethod) {
    app.writeln("import 'package:spry/spry.dart' show HttpMethod;");
  }

  for (final line in imports) {
    app.writeln(line);
  }
  if (hasDocsUi) {
    app.writeln("import 'package:spry/openapi.dart' show defineScalarHandler;");
  }

  app
    ..writeln()
    ..writeln('final app = Spry(')
    ..writeln('  caseSensitive: ${config.caseSensitive},')
    ..write(
      config.handlerCacheCapacity == null
          ? ''
          : '  handlerCacheCapacity: ${config.handlerCacheCapacity},\n',
    )
    ..writeln('  routes: {');

  final sortedPaths = routeGroups.keys.toList()..sort();
  for (final path in sortedPaths) {
    final entries = routeGroups[path]!..sort(_compareRouteEntries);
    app.writeln("    '${_escape(path)}': {");
    for (final entry in entries) {
      final key = entry.method == null ? 'null' : _methodLiteral(entry.method!);
      final alias = aliases[entry.filePath]!;
      app.writeln('      $key: ${_handlerLiteral(alias)},');
    }
    app.writeln('    },');
  }
  if (hasDocsUi) {
    final docsRoute = _escape(openapi!.ui!.route);
    app.writeln("    '$docsRoute': {");
    app.writeln('      HttpMethod.get: ${_scalarHandlerLiteral(openapi)},');
    app.writeln('    },');
  }

  app
    ..writeln('  },')
    ..writeln('  middleware: [');

  for (final entry in [...state.globalMiddleware, ...state.scopedMiddleware]) {
    final alias = aliases[entry.filePath]!;
    final methodPart = entry.method == null
        ? ''
        : ', method: ${_methodLiteral(entry.method!)}';
    app.writeln(
      "    MiddlewareRoute(path: '${_escape(entry.path)}'$methodPart, handler: $alias.middleware),",
    );
  }

  app
    ..writeln('  ],')
    ..writeln('  errors: [');

  for (final entry in state.scopedErrors) {
    final alias = aliases[entry.filePath]!;
    final methodPart = entry.method == null
        ? ''
        : ', method: ${_methodLiteral(entry.method!)}';
    app.writeln(
      "    ErrorRoute(path: '${_escape(entry.path)}'$methodPart, handler: $alias.onError),",
    );
  }

  app.writeln('  ],');

  if (state.fallback case final fallback?) {
    final alias = aliases[fallback.filePath]!;
    app
      ..writeln('  fallback: {')
      ..writeln('    null: ${_handlerLiteral(alias)},')
      ..writeln('  },');
  }

  if (config.target != BuildTarget.cloudflare) {
    app.writeln("  publicDir: '${_escape(config.publicDir)}',");
  }

  app.writeln(');');

  final hooksBuffer = StringBuffer()
    ..writeln("// Generated by spry - do not edit.");
  if (state.hooks case final hooks?) {
    hooksBuffer
      ..writeln(
        "import '${_relativeImport(hooks.filePath, from: genDir)}' as \$source;",
      )
      ..writeln()
      ..writeln(
        hooks.hasOnStart
            ? 'final onStart = \$source.onStart;'
            : 'final onStart = null;',
      )
      ..writeln(
        hooks.hasOnStop
            ? 'final onStop = \$source.onStop;'
            : 'final onStop = null;',
      )
      ..writeln(
        hooks.hasOnError
            ? 'final onError = \$source.onError;'
            : 'final onError = null;',
      );
  } else {
    hooksBuffer
      ..writeln()
      ..writeln('final onStart = null;')
      ..writeln('final onStop = null;')
      ..writeln('final onError = null;');
  }

  final spec = buildTargetSpec(config);
  final main = _generateMain(spec);
  if (includeRuntime) {
    yield GeneratedEntry(
      type: GeneratedEntryType.runtimeSource,
      path: 'src/app.dart',
      content: app.toString(),
    );
    yield GeneratedEntry(
      type: GeneratedEntryType.runtimeSource,
      path: 'src/hooks.dart',
      content: hooksBuffer.toString(),
    );
    yield GeneratedEntry(
      type: GeneratedEntryType.runtimeSource,
      path: 'src/main.dart',
      content: main,
    );
  }

  if (includeOpenApi) {
    final openapiEntry = generateOpenApiArtifact(state, config);
    if (openapiEntry != null) {
      yield openapiEntry;
    }
  }

  if (includeRuntime) {
    for (final file in spec.extraFiles) {
      yield file;
    }
  }

  if (includeClient) {
    yield* generateClientEntries(state, config);
  }
}

String _scalarHandlerLiteral(OpenAPIConfig openapi) {
  final ui = openapi.ui!;
  final specUrl = '/${openapi.output.path.replaceFirst(RegExp(r'^/+'), '')}';
  final buffer = StringBuffer()
    ..writeln('defineScalarHandler(')
    ..writeln("        url: '${_escape(specUrl)}',")
    ..writeln(
      "        title: '${_escape(ui.title ?? openapi.document.info.title)}',",
    );
  if (ui.theme case final theme?) {
    buffer.writeln("        theme: '${_escape(theme)}',");
  }
  if (ui.layout case final layout?) {
    buffer.writeln("        layout: '${_escape(layout)}',");
  }
  buffer.write('      )');
  return buffer.toString();
}

int _compareRouteEntries(RouteEntry a, RouteEntry b) {
  if (a.method == null && b.method != null) {
    return -1;
  }
  if (a.method != null && b.method == null) {
    return 1;
  }
  return (a.method?.value ?? '').compareTo(b.method?.value ?? '');
}

String _handlerLiteral(String alias) => '$alias.handler';

String _escape(String value) =>
    value.replaceAll(r'$', r'\$').replaceAll("'", r"\'");

String _methodLiteral(HttpMethod method) => 'HttpMethod.${method.name}';

String _relativeImport(String filePath, {required String from}) {
  return p.relative(filePath, from: from).replaceAll('\\', '/');
}

String _generateMain(TargetSpec spec) {
  final buffer = StringBuffer()
    ..writeln("// Generated by spry - do not edit.")
    ..writeln("import 'package:spry/osrv.dart';")
    ..writeln(spec.runtimeImport);
  buffer
    ..writeln("import 'hooks.dart' as \$hooks;")
    ..writeln("import 'app.dart';")
    ..writeln()
    ..write(spec.mainBody);
  return buffer.toString();
}
